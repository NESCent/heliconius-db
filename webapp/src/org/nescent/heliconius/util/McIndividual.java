package org.nescent.heliconius.util;

public class McIndividual {
	String Collection_No;
	String Collection_Date;
	String Genus;
	String Species;
	String Race;
	String Sex;
	String General_Notes;
	String WildOrReared;
	/**
	 * @return the collection_Date
	 */
	public String getCollection_Date() {
		return Collection_Date;
	}
	/**
	 * @param collection_Date the collection_Date to set
	 */
	public void setCollection_Date(String collection_Date) {
		Collection_Date = collection_Date;
	}
	/**
	 * @return the collection_No
	 */
	public String getCollection_No() {
		return Collection_No;
	}
	/**
	 * @param collection_No the collection_No to set
	 */
	public void setCollection_No(String collection_No) {
		Collection_No = collection_No;
	}
	/**
	 * @return the general_Notes
	 */
	public String getGeneral_Notes() {
		return General_Notes;
	}
	/**
	 * @param general_Notes the general_Notes to set
	 */
	public void setGeneral_Notes(String general_Notes) {
		General_Notes = general_Notes;
	}
	/**
	 * @return the genus
	 */
	public String getGenus() {
		return Genus;
	}
	/**
	 * @param genus the genus to set
	 */
	public void setGenus(String genus) {
		Genus = genus;
	}
	/**
	 * @return the race
	 */
	public String getRace() {
		return Race;
	}
	/**
	 * @param race the race to set
	 */
	public void setRace(String race) {
		Race = race;
	}
	/**
	 * @return the sex
	 */
	public String getSex() {
		return Sex;
	}
	/**
	 * @param sex the sex to set
	 */
	public void setSex(String sex) {
		Sex = sex;
	}
	/**
	 * @return the species
	 */
	public String getSpecies() {
		return Species;
	}
	/**
	 * @param species the species to set
	 */
	public void setSpecies(String species) {
		Species = species;
	}
	/**
	 * @return the wildOrReared
	 */
	public String getWildOrReared() {
		return WildOrReared;
	}
	/**
	 * @param wildOrReared the wildOrReared to set
	 */
	public void setWildOrReared(String wildOrReared) {
		WildOrReared = wildOrReared;
	}

}
